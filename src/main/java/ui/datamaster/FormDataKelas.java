    private void cbkKelasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbkKelasItemStateChanged
        // TODO add your handling code here:
        try {
            if (cbkKelas.getSelectedIndex() >= 0) {
                Kelas kelas = listKelas.get(cbkKelas.getSelectedIndex());
                txtKelas.setText(kelas.getNamaKelas());
                
                ServiceOfKelas service = new ServiceOfKelas(HIbernateUtil.config());
                List<Siswa> siswa;
                try {
                    controll.initTable();
                    siswa = service.findSiswaByKelas(kelas);
                    System.out.println("jumlah data siswa ditemukan " + siswa.size());
                    for (Siswa aSiswa : siswa) {
                       // for (int i = 0; i < aSiswa; i++)  {
                            Object[] value = {aSiswa.getKodeSiswa(), aSiswa.getNama()};
                            controll.getDefaultTableModel().addRow(value);
                        //}
                    }
                } catch (Exception ex) {
                  //  Logger.getLogger(FormPenilaian.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            } else {
                txtKelas.setText("");
            }
        } catch (NullPointerException npe) {
            npe.printStackTrace();
        }
    }//GEN-LAST:event_cbkKelasItemStateChanged


